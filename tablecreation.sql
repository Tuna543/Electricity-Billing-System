DROP TABLE COMPLAINT;
DROP TABLE TRANSACTIONS;
DROP TABLE BILL;
DROP TABLE BOARD;
DROP TABLE CUSTOMERS;

--LAB 01:

CREATE TABLE CUSTOMERS(
    CUST_ID INTEGER PRIMARY KEY,
    C_NAME VARCHAR(100),
    EMAIL VARCHAR(100) unique, --lab 03 (using unique key constraints)
    PASS VARCHAR(100),
    ADDRESS VARCHAR(100)
);

CREATE TABLE BILL(
    BILL_ID INTEGER PRIMARY KEY,
    AMOUNT INTEGER,
    STAT VARCHAR(100),
    CUST_ID INTEGER NOT NULL unique, --lab 03 (using unique key constraints)
    FOREIGN KEY (CUST_ID) references CUSTOMERS(CUST_ID)
);

CREATE TABLE BOARD(
  BOARD_ID INTEGER PRIMARY KEY,
  BOARD_NAME VARCHAR(40) NOT NULL,
  PASS VARCHAR(20) NOT NULL
);

CREATE TABLE TRANSACTIONS(
	T_ID INTEGER PRIMARY KEY,
	DATE_PAY VARCHAR(100) NOT NULL,
	PAYABLE VARCHAR(100) NOT NULL,
  BILL_ID INTEGER NOT NULL unique,
  FOREIGN KEY (BILL_ID) references BILL(BILL_ID)
);

CREATE TABLE COMPLAINT(
  COM_ID INTEGER NOT NULL,
  CUST_ID INTEGER NOT NULL,
  BOARD_ID INTEGER,
  COM VARCHAR(100) NOT NULL,
  STAT VARCHAR(100) NOT NULL
);
