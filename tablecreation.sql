--LAB 01:

CREATE TABLE CUSTOMERS(
    CUST_ID INTEGER PRIMARY KEY,
    C_NAME VARCHAR(100),
    EMAIL VARCHAR(100) unique, --lab 03 (using unique key constraints)
    PASS VARCHAR(100),
    ADDRESS VARCHAR(100)
);


CREATE TABLE BILL(
    BILL_ID INTEGER PRIMARY KEY,
    AMOUNT INTEGER,
    STATUS VARCHAR(100),
    CUST_ID INTEGER NOT NULL unique --lab 03 (using unique key constraints)
);

CREATE TABLE BOARD(
  BOARD_ID INTEGER NOT NULL,
  BOARD_NAME VARCHAR(40) NOT NULL,
  PASS VARCHAR(20) NOT NULL
);

CREATE TABLE transactions(
	T_ID INTEGER NOT NULL,
	DATE_PAY VARCHAR(100) NOT NULL,
	PAYABLE VARCHAR(100) NOT NULL,
  BILL_ID INTEGER NOT NULL unique
);

